let buildsArray = [
  gutsSword,
  giantCrusher,
  maraisExecutionersSword,
  darkmoonGreatsword,
  deathsPoker,
  moonveil,
  handOfMalenia,
  uchigatana,
  boltOfGransax,
  blasphemousBlade,
  sacredRelicSword,
  godslayersGreatsword,
  riversOfBlood,
  dragonCommunion,
  mohgwynsSacredSpear,
];

export default function buildStructs(stat: string | undefined) {
  let validBuilds;
  switch (stat) {
    case "int": {
      validBuilds = [darkmoonGreatsword, deathsPoker, moonveil];
      break;
    }
    case "str": {
      // **HINT HINT**
      validBuilds = [gutsSword, boltOfGransax, maraisExecutionersSword];
      break;
    }
    case "dex": {
      validBuilds = [handOfMalenia, uchigatana, boltOfGransax];
      break;
    }
    case "fai": {
      validBuilds = [blasphemousBlade, sacredRelicSword, godslayersGreatsword];
      break;
    }
    case "arc": {
      validBuilds = [riversOfBlood, dragonCommunion, mohgwynsSacredSpear];
      break;
    }
    case "rand": {
      buildsArray.sort(() => Math.random() - 0.5);
      validBuilds = [buildsArray[0], buildsArray[1], buildsArray[2]];
      break;
    }
    default: {
      validBuilds = undefined;
      break;
    }
  }
  return validBuilds;
}
